// Russian equivalent of buildCoreRules() — universal writing rules for Russian fiction
export function buildRussianCoreRules(_book: BookConfig): string {
  return `## Универсальные правила письма

### Характеры
1. **Согласованность**: поведение определяется тройкой «прошлый опыт + текущие интересы + ядро личности». Без причины характер не ломается.
2. **Объёмность**: основной штрих + контрастная деталь = живой человек. Безупречные персонажи — это провальные персонажи.
3. **Никаких марионеток**: у второстепенных есть собственные мотивы и право голоса. Сила протагониста — переигрывать умных, а не давить дураков.
4. **Различимость голосов**: разные персонажи говорят по-разному — лексика, длина фраз, словечки, мелкие речевые тики.
5. **Логика отношений**: любое изменение отношений должно подкрепляться событиями и интересами, а не происходить просто так.

### Нарративная техника
6. **Show, don't tell**: передавай через действие и сенсорную деталь, а не через объяснение. Ценности проявляются в поведении, а не декларируются.
7. **Сенсорное укоренение**: в каждой сцене 1–2 сенсорных детали кроме визуала.
8. **Крючки в финале главы**: каждая глава заканчивается на крючке — вопрос, разоблачение, угроза, обещание.
9. **Послойный ввод информации**: устройство мира раскрывается через действие. Ключевая информация — на сюжетных пиках. Никаких info-dump.
10. **Описание служит сюжету**: описание среды задаёт настроение или предвосхищает поворот. Одной строки достаточно.
11. **Передышки тоже работают**: спокойные сцены закладывают крючки, развивают отношения или строят контраст. Чистый филлер — пустой звук.
12. **Диалог ведёт сцену**: в сценах с взаимодействием конфликт и информация передаются через диалог в первую очередь, повествование — во вторую. Сольные сцены / побег / разведка — исключение.

### Логика и согласованность
13. **Правила мира — закон**: однажды установленные физические, магические, социальные правила не гнутся ради удобства сюжета.
14. **Цена имеет значение**: у любой силы, способности или преимущества должна быть цена или ограничение, создающее реальный размен.
15. **Последствия остаются**: у действий есть последствия. Персонажи не уходят от расплаты по удаче или авторскому произволу.
16. **Без кнопки «сброс»**: мир должен меняться навсегда после крупных событий.

### Психология читателя
17. **Обещание и расплата**: каждый заложенный крючок должен быть закрыт. У каждой загадки должна быть разгадка.
18. **Эскалация**: каждый следующий конфликт ощутимо выше предыдущего — внешне или эмоционально.
19. **Прокси читателя**: один из персонажей реагирует удивлением / страхом / восторгом, когда происходит редкое — это даёт читателю разрешение чувствовать то же самое.
20. **Дыхание темпа**: после высокоинтенсивной сцены — 0,5–1 главы низкой интенсивности перед следующим витком.`;
}

// Russian equivalent of buildAntiAIExamples()
export function buildRussianAntiAIRules(): string {
  return `## Железные законы против AI-следа

**[ЗАКОН 1]** Рассказчик никогда не подсказывает читателю выводы.
Если читатель может вывести намерение из действия — рассказчик не имеет права его называть.
- ✗ «Он понял, что это самая важная битва в его жизни.»
- ✓ Просто опиши битву — пусть ставка говорит сама.

**[ЗАКОН 2]** Никакой аналитической / отчётной речи в художественном тексте.
Запрещено в нарративе: «ключевая мотивация», «асимметрия информации», «стратегическое преимущество», «зона риска», «оптимальный исход», «стоит отметить», «следует подчеркнуть», «вместе с тем», «в то же время».
- ✗ «Его ключевая мотивация — выживание.»
- ✓ «Ему надо было выбраться. И всё. Остальное — шум.»

**[ЗАКОН 3]** AI-маркерные обороты идут с лимитом (максимум 1 на 3000 слов):
«тем не менее», «стоит отметить», «следует подчеркнуть», «вместе с тем», «в то же время», «не просто X, а Y», «своего рода», «как бы», «определённый» (как обтекаемое прилагательное), «непосредственно», «безусловно», «определённо», «во многом».

**[ЗАКОН 4]** Никаких повторов одной и той же образной системы.
Если метафора прозвучала дважды, на третьем повторении переключайся на новый образ.

**[ЗАКОН 5]** Аналитические термины не появляются в тексте главы.
«Текущее положение», «ключевая мотивация», «граница информации» — это инструменты PRE_WRITE_CHECK, и только.

**[ЗАКОН 6]** Запрет на конструкцию «не X, а Y» / «не просто X, но Y». Максимум один раз на главу.
- ✗ «Это был не страх. Это было что-то глубже.»
- ✓ Назови вещь напрямую.

**[ЗАКОН 7]** Запрет на тройки в описаниях. Не чаще одного раза на 2000 слов.
- ✗ «древний, ужасный и огромный»
- ✓ Используй пары или одно точное слово.

**[ЗАКОН 8]** Запрет на тире-связку «—» как универсальный знак паузы. Используй точки, запятые, точку с запятой, абзац. Тире только там, где оно жёстко правомерно (прямая речь, диапазон, авторская реплика внутри прямой речи).

### Таблица «AI → человек»

| AI-паттерн | Человеческий вариант | Почему |
|---|---|---|
| Он почувствовал прилив гнева. | Он ударил по столу. Стакан опрокинулся. | Действие выносит эмоцию наружу |
| Её захлестнула печаль. | Она держала телефон обеими руками, костяшки побелели. | Физическая деталь вместо ярлыка |
| Однако всё было не так просто. | Ага, как же. Так не бывает. | Голос персонажа вместо страховки рассказчика |
| Он увидел тень, скользнувшую по стене. | По стене скользнула тень. | Убрали слово-фильтр «увидел» |
| «Я не сделаю этого», — упрямо воскликнула она. | «Я не сделаю этого». Она скрестила руки. | Жест-маркер вместо наречия и пышного глагола |
| Стоит отметить, что он не доверял ей. | Он смотрел в её сторону, но не на неё. | Действие заменяет сигнальное «стоит отметить» |
| Тем не менее, он остался. | Он остался. | «Тем не менее» в художке — почти всегда лишнее |`;
}

// Russian equivalent of buildCharacterPsychologyMethod()
export function buildRussianCharacterMethod(): string {
  return `## Метод психологии персонажа (внутренний инструмент)

Перед тем как написать действие или реплику любого персонажа, прогони внутренний чек-лист (НЕ в тексте):
1. **Положение**: что этот персонаж знает ПРЯМО СЕЙЧАС? (граница информации)
2. **Желание**: чего он хочет в этой сцене? (ближайшая цель)
3. **Фильтр характера**: как его личность преломляет подход?
4. **Действие**: что он ДЕЛАЕТ? (поведение, не внутренний монолог)
5. **Реакция**: как окружающие отвечают на его действие?

Метод — для твоего планирования. Эти термины никогда не появляются в тексте главы.

### Тройной вопрос «не сломал ли я характер» (перед каждым действием персонажа)
1. «Зачем он это делает?» — должна быть выгода или эмоциональный двигатель.
2. «Это согласуется с его прошлыми сценами?» — поведение растёт из «опыт + интересы + характер».
3. «Если читатель помнит только предыдущие главы — это будет выглядеть резко?» — проверка на согласованность.

### Принцип «соль растворена в супе»
Амбиции и ценности персонажа не выкрикиваются. Они проступают через поведение.
- Антипример: «Я стану сильнейшим!» — пустой лозунг.
- Пример: пока другие сдаются, он молча тренируется лишние два часа — действие несёт амбицию.`;
}

// Russian pre-write checklist
export function buildRussianPreWriteChecklist(book: BookConfig, gp: GenreProfile): string {
  const items = [
    "Опора на план: какой пункт volume_outline эта глава продвигает?",
    "Точка зрения: чья перспектива? Удерживается ли она от начала до конца?",
    "Заложенный крючок: какой вопрос / угроза / обещание тянет читателя в следующую главу?",
    "Сенсорная опора: минимум 2 несвизуальных сенсорных детали в каждой крупной сцене.",
    "Согласованность характеров: каждое действие следует из заявленной мотивации?",
    "Граница информации: ни один персонаж не ссылается на сведения, которым не был свидетелем.",
    `Темп: цель главы — около ${book.chapterWordCount} слов. ${gp.pacingRule}`,
    "Show, don't tell: эмоции выражены через действие, а не названы прямо?",
    "AI-проверка: никаких аналитических ярлыков, «стоит отметить», «тем не менее», «вместе с тем» в тексте?",
    "Конфликт: какое центральное напряжение тянет эту главу?",
  ];

  if (gp.powerScaling) {
    items.push("Шкала силы: использование любой способности укладывается в установленные правила?");
  }
  if (gp.numericalSystem) {
    items.push("Числовая сверка: все статы / ресурсы согласованы с реестром?");
  }

  return `## Pre-Write Checklist

Перед написанием выпусти PRE_WRITE_CHECK, отвечая по пунктам:
${items.map((item, i) => `${i + 1}. ${item}`).join("\n")}`;
}

